import { test } from '@jest/globals'
import { testType } from '../index.js'

test('union behavior of any', () => {
	testType.equal<any | undefined, any>(true)
	testType.equal<any | null, any>(true)
	testType.equal<any | boolean, any>(true)
	testType.equal<any | true, any>(true)
	testType.equal<any | false, any>(true)
	testType.equal<any | number, any>(true)
	testType.equal<any | 1, any>(true)
	testType.equal<any | string, any>(true)
	testType.equal<any | '', any>(true)
	testType.equal<any | symbol, any>(true)
	testType.equal<any | bigint, any>(true)
	testType.equal<any | 1n, any>(true)
	testType.equal<any | {}, any>(true)
	testType.equal<any | { a: 1 }, any>(true)
	testType.equal<any | string[], any>(true)
	testType.equal<any | [], any>(true)
	testType.equal<any | Function, any>(true)
	testType.equal<any | (() => void), any>(true)

	testType.equal<any | any, any>(true)
	testType.equal<any | unknown, any>(true)
	testType.equal<any | never, any>(true)
	testType.equal<any | void, any>(true)
})

